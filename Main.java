public class Main {
    public static void main(String[] args) {
        int a =9;
        int b=3;
        System.out.println("We are learning bit-wise operator:)");
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(~b);
        System.out.println(a<<b);
        System.out.println(a>>b);

    }
}